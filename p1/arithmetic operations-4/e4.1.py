#E-4.1: Times five

#Problem: Please write a program which asks the user for a number.
#The program then prints out the number multiplied by five.

#The program should function as follows:

        #Please type in a number: 3
        #3 times 5 is 15

#Solution:

x = input("Please type in a number: ")
num = int(x)
print(f"{num} times 5 is {num * 5}")



